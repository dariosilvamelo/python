"""
Tipo tupla - Uma lista imutável
"""
# é uma lista que não pode ser alterada
nomes1 = ('Maria', 'Helena', 'Luiz')
# ou
nomes2 = 'Dario', 'Carulina', 'Patricia'

# converte uma lista em tupla e voltat para lusta

nomes3 = ['Clariano', 'Lusmar', 'Joao']
nomes4 = tuple(nomes3)
nomes5 = list(nomes4)

print(nomes1)
print(nomes2)
print(nomes3)
print(nomes4)
print(nomes5)
print(nomes1[1])
